/**
 * 課題IDから課題情報を取得する
 * @param {string} id - 課題ID
 * @returns {Object|null} - 課題情報、見つからない場合はnull
 */
export function getAssignmentById(id) {
  return assignmentsData.find(assignment => assignment.id === id) || null;
}

/**
 * 科目に属する課題を取得する
 * @param {string} subject - 科目ID
 * @returns {Array} - 該当する科目の課題配列
 */
export function getAssignmentsBySubject(subject) {
  return assignmentsData.filter(assignment => assignment.subject === subject);
} 